package Pattern_question;
import java.util.*;
public class Print_thePattern 
{

		public static void main(String[] args) 
	{
		int n=4;
		int m=10;
		
				for( int i=1;i<=n;i++)
				{
					
				for(int j=0;j<m;j++)
				{
					
				System.out.print("*");
				}
				System.out.println();
				}
	

	}
}
